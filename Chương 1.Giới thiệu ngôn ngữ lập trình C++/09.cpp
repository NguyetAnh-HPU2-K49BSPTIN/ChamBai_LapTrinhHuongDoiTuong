#include <iostream>
using namespace std;

int main()
{
	// kích thước của các kiểu dữ liệu
	cout << "char " << sizeof(char) << " byte" << endl;
	cout << "int " << sizeof(int) << " byte" << endl;
	cout << "float " << sizeof(float) << " byte" << endl;
	cout << "double " <<sizeof(double) << " byte" << endl;

	return 0;
}
